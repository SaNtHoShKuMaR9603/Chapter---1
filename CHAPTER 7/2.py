# Loop from 0 to 9
for i in range(0,10):
    # If i equals 5, exit the loop
    if i == 5:
        break
    # Print the current value of i
    print(i)
    

# Loop from 0 to 9
for i in range(0,10):
    # If i equals 5, skip this iteration
    if i == 5:
        continue
    # Print the current value of i
    print(i)
    

# Loop from 0 to 9
for i in range(0,10):
    # If i equals 5, do nothing (pass statement)
    if i == 5:
        pass
    # Print the current value of i
    print(i)